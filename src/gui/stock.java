    public void loadProductTable() {
        try {
            ResultSet resultset = model.MySQL.execute("SELECT * FROM `product` INNER JOIN `brand` ON `product`.`brand_id`=`brand`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultset.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultset.getString("product.id"));
                vector.add(resultset.getString("product.name"));
                vector.add(resultset.getString("brand.name"));
                model.addRow(vector);
            }

        } catch (Exception e) {
        }
    }

    public void loadStockTable() {

        try {
            ResultSet resultset = model.MySQL.execute("SELECT * FROM `stock` INNER JOIN `product` ON `stock`.`product_id`=`product`.`id` INNER JOIN `brand` ON `product`.`brand_id`=`brand`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (resultset.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultset.getString("stock.id"));
                vector.add(resultset.getString("product.id"));
                vector.add(resultset.getString("product.name"));
                vector.add(resultset.getString("brand.name"));
                vector.add(resultset.getString("stock.mfd"));
                vector.add(resultset.getString("stock.exp"));
                vector.add(resultset.getString("stock.qty"));
                vector.add(resultset.getString("stock.selling_price"));

                model.addRow(vector);
            }

        } catch (Exception e) {
        }
    }

    public void loadBrandComboBox() {

        try {
            ResultSet resultset = model.MySQL.execute("SELECT * FROM `brand`");
            Vector<String> vector = new Vector<>();
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);

            while (resultset.next()) {
                vector.add(resultset.getString("name"));
            }
            jComboBox1.setModel(model);

        } catch (Exception e) {
        }

    }

